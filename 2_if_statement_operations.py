# Operations +, -, /, //, %, **
print(1+2)
print(1-2)
print(3/2)
print(3//2)
print(9%2)
print(9**2)

# Comparing with == and !=, inequalities
print(1 == 2)
print(1 != 2)
print(2 <= 2)

# Using operators with equal signs (+=, -=)
a = 2
a -= 1
print(a)

# If statement demonstration
b = 6
c = 6

if b < c:
    print("less")
elif b > c:
    print("more")
else:
    print("equal")
