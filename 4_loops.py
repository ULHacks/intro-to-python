# For loops with `start`, `end`, `skip`
for i in range(6):
    print(i)

for i in range(2, 27, 2):
    print(i)

for i in range(26, 1, -2):
    print(i)

# For loop to loop through an array
li = ["3", 1, 2, "3", "four"]

for i in li:
    print(i)

# Basic use of a while loop
a = 2

while a <= 28:
    print(a)
    a += 2
